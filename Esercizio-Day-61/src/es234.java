public class es234 {

    public String stringifier(int number) {
        switch (number) {
            case 0:
                return "zero";
            case 1:
                return "uno";
            case 2:
                return "due";
            case 3:
                return "tre";
            case 4:
                return "quattro";
            case 5:
                return "cinque";
            default:
                return "numero sconosciuto";
        }
    }

}
